package com.kacperbieganek.dao.model;

public class Flagella {
    private int alphaGene;
    private int betaGene;
    private int rank;

    public int getAlfaGene() {
        return alphaGene;
    }

    public void setAlfaGene(int alphaGene) {
        this.alphaGene = alphaGene;
    }

    public int getBetaGene() {
        return betaGene;
    }

    public void setBetaGene(int betaGene) {
        this.betaGene = betaGene;
    }

    public int getRank() {
        return rank;
    }

    public void setRank(int rank) {
        this.rank = rank;
    }
}
